% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
startX=get(handles.edit1,'string');
startY=get(handles.edit2,'string');
width=get(handles.edit3,'string');
height=get(handles.edit4,'string');
startZ=get(handles.edit5,'string');
endZ=get(handles.edit6,'string');
pre=get(handles.edit9,'string');
post=get(handles.edit10,'string');

startX=str2num(startX);
startY=str2num(startY);
width=str2num(width);
height=str2num(height);
startZ=str2num(startZ);
endZ=str2num(endZ);
src=get(handles.edit7,'string');
dst=get(handles.edit8,'string');
src=[src '\'];
dst=[dst '\'];

wPre='test_';
wMid=['_x' num2str(startX) '_y' num2str(startY) '_w' num2str(width) '_h' num2str(height)];
wPost='.tif';

tmpImage=imread([src pre num2str(startZ,'%05d') post]);
[rawImageHeight,rawImageWidth]=size(tmpImage);
clear tmpImage;


inImage=zeros(rawImageHeight,rawImageWidth);
outImage=zeros(height,width);
for i=startZ:endZ
    inImage=imread([src pre num2str(i,'%05d') post]);
    disp(['reading ' num2str(i,'%05d') ' image']);
    outImage=inImage(startY:startY+height-1,startX:startX+width-1);
    disp(['writing ' num2str(i,'%05d') ' image'])
    imwrite(outImage,[dst wPre num2str(i,'%05d') wMid wPost]);
end















